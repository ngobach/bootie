struct color {
    uint8_t r, g, b;
};



static inline struct color get_color(char c) {
    switch (c) {
        case '.': return (struct color){255, 255, 255}; // Star / Eye sparkle (White)
        case '\'': return (struct color){0, 0, 0};      // Black outline
        case '@': return (struct color){255, 204, 153}; // Crust
        case '$': return (struct color){255, 153, 255}; // Pink frosting
        case '-': return (struct color){255, 51, 153};  // Dark pink sprinkles
        case '*': return (struct color){153, 153, 153}; // Grey skin
        case '%': return (struct color){255, 153, 153}; // Light pink cheeks
        case '>': return (struct color){255, 0, 0};     // Red
        case '&': return (struct color){255, 153, 0};   // Orange
        case '+': return (struct color){255, 255, 0};   // Yellow
        case '#': return (struct color){51, 255, 0};    // Green
        case '=': return (struct color){0, 153, 255};   // Blue
        case ';': return (struct color){102, 51, 255};  // Purple
        case ',': // Background
        default:
            return (struct color){15, 77, 143};         // Nyan background blue
    }
}

/* Draws a frame using horizontal run-length optimization */
static void draw_nyan_frame(struct gfx *g, const char frame[64][64], int x_off, int y_off, int scale) {
    for (int r = 0; r < 64; r++) {
        const char *line = frame[r];
        int ry = y_off + r * scale;
        int c = 0;
        while (c < 64) {
            char val = line[c];
            int count = 1;
            while (c + count < 64 && line[c + count] == val) {
                count++;
            }
            struct color col = get_color(val);
            int rx = x_off + c * scale;
            fill_rect(g, rx, ry, count * scale, scale, col.r, col.g, col.b);
            c += count;
        }
    }
}

int gmain(int argc, char *argv[], int flags) {
    (void)argc;
    (void)argv;
    (void)flags;

    struct gfx g;
    if (!gfx_init(&g)) {
        return 0;
    }

    uint32_t W = gfx_width(&g);
    uint32_t H = gfx_height(&g);

    int scale = 8;
    // Downscale slightly for smaller screens
    if (H < 512 + 40) {
        scale = 6;
    }

    int frame_size = 64 * scale;
    int x_off = (W - frame_size) / 2;
    int y_off = (H - frame_size) / 2;

    /* Fill background initially */
    fill_rect(&g, 0, 0, W, H, 15, 77, 143);

    int current_frame = 0;
    struct bt_fps fps;
    bt_fps_init(&fps, 12);

    while (1) {
        /* Check if user wants to exit */
        if (gfx_checkkey(&g)) {
            int key = gfx_getkey(&g);
            if (key == 27) { // ESC to exit
                break;
            }
        }

        /* Render frame */
        gfx_backbuffer_begin(&g);
        draw_nyan_frame(&g, frames[current_frame], x_off, y_off, scale);
        gfx_backbuffer_end(&g);

        current_frame = (current_frame + 1) % 12;

        /* Standard Nyan Cat animation rate is ~80ms per frame */
        bt_fps_wait(&fps);
    }

    gfx_close(&g);
    return 0;
}
