type Exfat struct {
	dev            io.ReadWriteSeeker
	sb             exfatSuperBlock
	upcase         []uint16
	root           *exfatNode
	cmapStart      uint32
	cmapSize       uint32
	cmapChunk      []uint64
	cmapDirty      bool
	label          string
	mounted        bool
}

func (e *Exfat) Type() filesystem.Type { return filesystem.TypeFat32 }

func (e *Exfat) Mkdir(path string) error {
	return e.mkdir(path)
}

func (e *Exfat) Mknod(path string, mode uint32, dev int) error {
	return e.mknod(path)
}

func (e *Exfat) Link(string, string) error    { return filesystem.ErrNotSupported }
func (e *Exfat) Symlink(string, string) error  { return filesystem.ErrNotSupported }

func (e *Exfat) Chmod(name string, mode os.FileMode) error {
	return e.chmod(name, mode)
}

func (e *Exfat) Chown(name string, uid, gid int) error {
	return e.chown(name, uid, gid)
}

func (e *Exfat) ReadDir(path string) ([]os.FileInfo, error) {
	return e.readDir(path)
}

func (e *Exfat) OpenFile(path string, flag int) (filesystem.File, error) {
	return e.openFile(path, flag)
}

func (e *Exfat) Rename(oldPath, newPath string) error {
	return e.rename(oldPath, newPath)
}

func (e *Exfat) Remove(path string) error {
	return e.remove(path)
}

func (e *Exfat) Label() string {
	return e.label
}

func (e *Exfat) SetLabel(label string) error {
	return e.setLabel(label)
}

func (e *Exfat) Close() error {
	return e.close()
}

func NewExfat(rw io.ReadWriteSeeker) (*Exfat, error) {
	e := &Exfat{dev: rw}
	if err := e.mount(); err != nil {
		return nil, err
	}
	return e, nil
}

func CreateExfat(w io.WriterAt, start int64, size int64, label string) error {
	if size < 1024*1024 {
		return fmt.Errorf("partition too small for exFAT: %d bytes (minimum 1 MiB)", size)
	}

	volumeSectors := uint64(size / 512)
	partitionStart := uint64(start / 512)

	spcBits := exfatSpcBits(volumeSectors)
	spc := uint64(1 << spcBits)

	fatOff, fatLen, heapOff, clusterCount := exfatLayout(volumeSectors, spcBits)
	fatOffset := uint64(fatOff)
	heapOffset := uint64(heapOff)
	totalClusters := uint64(clusterCount)
	bytesPerCluster := spc * 512

	upcaseSize := uint64(defaultUpcaseSize)
	upcaseClusters := (upcaseSize + bytesPerCluster - 1) / bytesPerCluster

	bitmapSize := (totalClusters + 7) / 8
	bitmapClusters := (bitmapSize + bytesPerCluster - 1) / bytesPerCluster

	rootCluster := uint32(2)
	bitmapFirstCluster := rootCluster + 1
	upcaseFirstCluster := bitmapFirstCluster + uint32(bitmapClusters)

	neededClusters := 1 + bitmapClusters + upcaseClusters
	if totalClusters < neededClusters {
		return fmt.Errorf("volume too small: need at least %d clusters for metadata, have %d",
			neededClusters, totalClusters)
	}

	serial := rand.Uint32()

	boot := make([]byte, 512)
	boot[0] = 0xEB
	boot[1] = 0x76
	boot[2] = 0x90
	copy(boot[3:11], "EXFAT   ")
	binary.LittleEndian.PutUint64(boot[64:72], partitionStart)
	binary.LittleEndian.PutUint64(boot[72:80], volumeSectors)
	binary.LittleEndian.PutUint32(boot[80:84], uint32(fatOffset))
	binary.LittleEndian.PutUint32(boot[84:88], uint32(fatLen))
	binary.LittleEndian.PutUint32(boot[88:92], uint32(heapOffset))
	binary.LittleEndian.PutUint32(boot[92:96], uint32(clusterCount))
	binary.LittleEndian.PutUint32(boot[96:100], uint32(rootCluster))
	binary.LittleEndian.PutUint32(boot[100:104], serial)
	boot[104] = 0
	boot[105] = 1
	boot[108] = 9
	boot[109] = spcBits
	boot[110] = 1
	boot[111] = 0x80
	boot[112] = 0xFF
	boot[510] = 0x55
	boot[511] = 0xAA

	bootSectors := make([][]byte, 12)
	bootSectors[0] = boot
	for i := 1; i < 11; i++ {
		bootSectors[i] = make([]byte, 512)
	}
	bootSectors[11] = make([]byte, 512)

	checksum := exfatBootChecksum(bootSectors[:11])
	for i := 0; i < 512; i += 4 {
		binary.LittleEndian.PutUint32(bootSectors[11][i:], checksum)
	}

	for i := 0; i < 12; i++ {
		off := int64(partitionStart*512 + uint64(i)*512)
		if _, err := w.WriteAt(bootSectors[i], off); err != nil {
			return fmt.Errorf("failed to write boot sector %d: %w", i, err)
		}
	}

	for i := 0; i < 12; i++ {
		off := int64(partitionStart*512 + 12*512 + uint64(i)*512)
		if _, err := w.WriteAt(bootSectors[i], off); err != nil {
			return fmt.Errorf("failed to write backup boot sector %d: %w", i, err)
		}
	}

	fatBytes := uint64(fatLen) * 512
	fat := make([]byte, fatBytes)
	binary.LittleEndian.PutUint32(fat[0:4], 0xFFFFFFF8)
	binary.LittleEndian.PutUint32(fat[4:8], 0xFFFFFFFF)

	for i := uint32(2); i < 2+uint32(neededClusters); i++ {
		binary.LittleEndian.PutUint32(fat[i*4:(i+1)*4], 0xFFFFFFFF)
	}

	fatSectorStart := partitionStart*512 + fatOffset*512
	if _, err := w.WriteAt(fat, int64(fatSectorStart)); err != nil {
		return fmt.Errorf("failed to write FAT: %w", err)
	}

	heapSectorStart := partitionStart*512 + heapOffset*512

	rootDirBytes := make([]byte, bytesPerCluster)
	labelEntry := exfatLabelEntry(label)
	copy(rootDirBytes[0:32], labelEntry)

	bitmapEntry := make([]byte, 32)
	bitmapEntry[0] = 0x81
	bitmapEntry[1] = 0
	binary.LittleEndian.PutUint32(bitmapEntry[20:24], bitmapFirstCluster)
	binary.LittleEndian.PutUint64(bitmapEntry[24:32], bitmapSize)
	copy(rootDirBytes[32:64], bitmapEntry)

	upcaseEntry := make([]byte, 32)
	upcaseEntry[0] = 0x82
	binary.LittleEndian.PutUint32(upcaseEntry[4:8], 0xAFB056ED)
	binary.LittleEndian.PutUint32(upcaseEntry[20:24], upcaseFirstCluster)
	binary.LittleEndian.PutUint64(upcaseEntry[24:32], upcaseSize)
	copy(rootDirBytes[64:96], upcaseEntry)

	rootSector := heapSectorStart + uint64(rootCluster-2)*bytesPerCluster
	if _, err := w.WriteAt(rootDirBytes, int64(rootSector)); err != nil {
		return fmt.Errorf("failed to write root directory: %w", err)
	}

	bitmapBytes := make([]byte, bitmapClusters*bytesPerCluster)
	for i := uint64(0); i < neededClusters; i++ {
		bitmapBytes[i/8] |= 1 << (i % 8)
	}

	bitmapSector := heapSectorStart + uint64(bitmapFirstCluster-2)*bytesPerCluster
	if _, err := w.WriteAt(bitmapBytes, int64(bitmapSector)); err != nil {
		return fmt.Errorf("failed to write allocation bitmap: %w", err)
	}

	upcaseSector := heapSectorStart + uint64(upcaseFirstCluster-2)*bytesPerCluster
	if _, err := w.WriteAt(defaultUpcaseTable[:], int64(upcaseSector)); err != nil {
		return fmt.Errorf("failed to write upcase table: %w", err)
	}

	return nil
}

func exfatSpcBits(volumeSectors uint64) uint8 {
	volBytes := volumeSectors * 512
	switch {
	case volBytes < 512*1024*1024:
		return 3
	case volBytes < 128*1024*1024*1024:
		return 6
	default:
		return 9
	}
}

func exfatLayout(volumeSectors uint64, spcBits uint8) (fatOffset, fatLen, heapOffset, clusterCount uint32) {
	fatOffset = 24
	spc := uint64(1 << spcBits)

	clusterCount = uint32((volumeSectors - uint64(fatOffset)) / spc)

	for range 5 {
		fatLen = uint32((uint64(clusterCount+2)*4 + 511) / 512)
		heapOffset = fatOffset + fatLen
		clusterCount = uint32((volumeSectors - uint64(heapOffset)) / spc)
	}

	return
}

func exfatBootChecksum(sectors [][]byte) uint32 {
	var sum uint32
	for si, sector := range sectors {
		for i, b := range sector {
			if si == 0 && (i == 106 || i == 107 || i == 112) {
				continue
			}
			sum = ((sum << 31) | (sum >> 1)) + uint32(b)
		}
	}
	return sum
}

func exfatLabelEntry(label string) []byte {
	runes := []rune(label)
	if len(runes) > 11 {
		runes = runes[:11]
	}

	entry := make([]byte, 32)
	entry[0] = 0x83
	entry[1] = uint8(len(runes))

	for i, r := range runes {
		if i >= 11 {
			break
		}
		binary.LittleEndian.PutUint16(entry[2+i*2:], uint16(r))
	}

	return entry
}

